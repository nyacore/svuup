    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $event = new Event($request->all());
        $event->save();

        $discipline = Discipline::find($event->discipline_id);

        $discipline->specialities->each(function ($speciality) use ($event) {
            $speciality->groups->each(function ($group) use ($event) {
                $group->users->each(function ($user) use ($event) {
                    (new Mark([
                        'user_id' => $user->id,
                        'event_id' => $event->id,
                        'mark' => 0
                    ]))->save();
                });
            });
        });

        return $this->httpCreated();
    }

